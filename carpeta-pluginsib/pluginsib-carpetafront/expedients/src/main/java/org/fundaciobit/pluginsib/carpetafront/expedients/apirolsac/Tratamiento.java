package org.fundaciobit.pluginsib.carpetafront.expedients.apirolsac;

public class Tratamiento{
    public String cargoF;
    public String cargoM;
    public String codigo;
    public String tipo;
    public String tratamientoF;
    public String tratamientoM;
    public String getCargoF() {
        return cargoF;
    }
    public void setCargoF(String cargoF) {
        this.cargoF = cargoF;
    }
    public String getCargoM() {
        return cargoM;
    }
    public void setCargoM(String cargoM) {
        this.cargoM = cargoM;
    }
    public String getCodigo() {
        return codigo;
    }
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }
    public String getTipo() {
        return tipo;
    }
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    public String getTratamientoF() {
        return tratamientoF;
    }
    public void setTratamientoF(String tratamientoF) {
        this.tratamientoF = tratamientoF;
    }
    public String getTratamientoM() {
        return tratamientoM;
    }
    public void setTratamientoM(String tratamientoM) {
        this.tratamientoM = tratamientoM;
    }
    
    
}
